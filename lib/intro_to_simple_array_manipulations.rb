def using_push(array, element)
  array << element
end

def using_unshift(array, element)
  array.unshift(element)
end

def using_pop(array)
  array.pop
end

def pop_with_args(array, number)
  array.pop(number)
end

def using_shift(array)
  array.shift
end

def shift_with_args(array, number)
  array.shift(number)
end

